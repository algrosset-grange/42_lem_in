#include "libft.h"

int		ft_woco(const char *s, char c)
{
	int		n;
	int		i;

	n = 0;
	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] != c && (s[i + 1] == c || s[i + 1] == '\0'))
			++n;
		++i;
	}
	return (n);
}
